FOR EACH ROW EXECUTE FUNCTION set_updated_at();

CREATE OR REPLACE FUNCTION aims_verify_staff_login(
    input_role TEXT,
    input_identifier TEXT,
    input_password TEXT
)
RETURNS TABLE (
    staff_id INTEGER,
    employee_id VARCHAR,
    full_name VARCHAR,
    email VARCHAR,
    role VARCHAR,
    status VARCHAR,
    created_at TIMESTAMP
)
LANGUAGE sql
SECURITY DEFINER
SET search_path = public
AS $$
    SELECT
        s.staff_id,
        s.employee_id,
        s.full_name,
        s.email,
        s.role,
        s.status,
        s.created_at
    FROM staff_accounts s
    WHERE LOWER(s.role) = LOWER(input_role)
      AND LOWER(s.status) = 'active'
      AND (
          LOWER(s.employee_id) = LOWER(input_identifier)
          OR LOWER(s.email) = LOWER(input_identifier)
      )
      AND (
          s.password_hash = input_password
          OR extensions.crypt(input_password, REPLACE(s.password_hash, '$2y$', '$2a$'))
              = REPLACE(s.password_hash, '$2y$', '$2a$')
      )
    LIMIT 1;
$$;

CREATE OR REPLACE FUNCTION aims_create_staff_account(
    input_employee_id TEXT,
    input_full_name TEXT,
    input_email TEXT,
    input_password TEXT,
    input_role TEXT,
    input_status TEXT
)
RETURNS TABLE (
    staff_id INTEGER,
    employee_id VARCHAR,
    full_name VARCHAR,
    email VARCHAR,
    role VARCHAR,
    status VARCHAR,
    created_at TIMESTAMP
)
LANGUAGE sql
SECURITY DEFINER
SET search_path = public
AS $$
    INSERT INTO staff_accounts (
        employee_id,
        full_name,
        email,
        password_hash,
        role,
        status
    )
    VALUES (
        UPPER(input_employee_id),
        input_full_name,
        LOWER(input_email),
        extensions.crypt(input_password, extensions.gen_salt('bf')),
        input_role,
        input_status
    )
    RETURNING
        staff_accounts.staff_id,
        staff_accounts.employee_id,
        staff_accounts.full_name,
        staff_accounts.email,
        staff_accounts.role,
        staff_accounts.status,
        staff_accounts.created_at;
$$;

CREATE OR REPLACE FUNCTION aims_update_staff_account(
    input_staff_id INTEGER,
    input_employee_id TEXT,
    input_full_name TEXT,
    input_email TEXT,
    input_password TEXT,
    input_role TEXT,
    input_status TEXT
)
RETURNS TABLE (
    staff_id INTEGER,
    employee_id VARCHAR,
    full_name VARCHAR,
    email VARCHAR,
    role VARCHAR,
    status VARCHAR,
    created_at TIMESTAMP
)
LANGUAGE sql
SECURITY DEFINER
SET search_path = public
AS $$
    UPDATE staff_accounts
    SET employee_id = UPPER(input_employee_id),
        full_name = input_full_name,
        email = LOWER(input_email),
        password_hash = CASE
            WHEN COALESCE(input_password, '') = '' THEN staff_accounts.password_hash
            ELSE extensions.crypt(input_password, extensions.gen_salt('bf'))
        END,
        role = input_role,
        status = input_status
    WHERE staff_accounts.staff_id = input_staff_id
    RETURNING
        staff_accounts.staff_id,
        staff_accounts.employee_id,
        staff_accounts.full_name,
        staff_accounts.email,
        staff_accounts.role,
        staff_accounts.status,
        staff_accounts.created_at;
$$;

GRANT EXECUTE ON FUNCTION aims_verify_staff_login(TEXT, TEXT, TEXT) TO anon, authenticated;
GRANT EXECUTE ON FUNCTION aims_create_staff_account(TEXT, TEXT, TEXT, TEXT, TEXT, TEXT) TO anon, authenticated;
GRANT EXECUTE ON FUNCTION aims_update_staff_account(INTEGER, TEXT, TEXT, TEXT, TEXT, TEXT, TEXT) TO anon, authenticated;

